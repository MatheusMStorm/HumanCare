    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        try {
            // TODO add your handling code here:
            String txtBusca = txtConsultaMed.getText();
            
            MedicoDAO medicoDAO = new MedicoDAO();
            
            List<Medico> listaMedicos = medicoDAO.consultarPorNome(txtBusca);
            
            DefaultTableModel modelo = (DefaultTableModel) tabMedicos.getModel();
            modelo.setNumRows(0);
            
            for (Medico m : listaMedicos) {
                modelo.addRow(new Object[]{
                    m.getNome_medico(),
                    m.getCrm_medico(),
                    m.getEspecialidade_medico(),
                    m.getTel_medico(),
                    m.getEmail_medico()
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegistroMedicos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBuscaActionPerformed


    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        try {                                             
            try {
                // TODO add your handling code here:
                Medico medico = new Medico(null, null, null, null, null);
                MedicoDAO medicoDAO = new MedicoDAO();
                
                medico.setNome_medico(txtNome.getText());
                medico.setCrm_medico(txtCrm.getText());
                medico.setEspecialidade_medico(txtEspecialidade.getText());
                medico.setTel_medico(txtTelMed.getText());
                medico.setEmail_medico(txtEmail.getText());
                
                medicoDAO.inserir(medico);
                JOptionPane.showMessageDialog(null, "Médico cadastrado com sucesso!");
            } catch (SQLException ex) {
                Logger.getLogger(RegistroMedicos.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Médico não cadastrado.");
            }
            
            preencherTabela();
        } catch (SQLException ex) {
            Logger.getLogger(RegistroMedicos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    
